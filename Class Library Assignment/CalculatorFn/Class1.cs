using System;
namespace CalculatorFn
{
    public class FnClassLibrary
    {
        public int DoSum(int firstNum, int secondNum)
        {
            return firstNum + secondNum;
        }

        public int DoSub(int firstNum, int secondNum)
        {
            return firstNum - secondNum;
        }

        public int DoMulti(int firstNum, int secondNum)
        {
            return firstNum * secondNum;
        }

        public int DoDivi(int firstNum, int secondNum)
        {
            return firstNum / secondNum;
        }
    }
}
